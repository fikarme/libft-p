#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	c;

	i = 0;
	s = 1;
	c = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = (10 * c) + (str[i] - '0');
		i++;
	}
	return (c * s);
}
